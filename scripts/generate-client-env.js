#!/usr/bin/env node

/**
 * This script generates client environment variables from the root .env file
 * It's meant to be run before building the client
 */

import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';
import { dirname } from 'path';
import dotenv from 'dotenv';

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);

// Load environment variables from root .env file
const rootEnvPath = path.resolve(__dirname, '../.env');
const envConfig = dotenv.parse(fs.readFileSync(rootEnvPath));

// Create client .env file content
const clientEnvContent = `# This file is auto-generated from the root .env file
# Do not edit this file directly

# API URL with port from root .env
VITE_API_URL=http://localhost:${envConfig.PORT || '4000'}
`;

// Write to client .env file
const clientEnvPath = path.resolve(__dirname, '../client/.env');
fs.writeFileSync(clientEnvPath, clientEnvContent);

console.log(`âœ… Generated client environment variables at ${clientEnvPath}`);
console.log(`API URL set to: http://localhost:${envConfig.PORT || '4000'}`); 